def get_palette(theme_name: str) -> dict:
    """Get palette for a theme. Falls back to dark."""
    return THEME_PALETTES.get(theme_name, THEME_PALETTES["dark"])


def get_all_theme_names() -> list:
    """Get all available theme names."""
    return list(THEME_PALETTES.keys())


def get_dark_themes() -> list:
    """Get names of dark themes."""
    return [k for k, v in THEME_PALETTES.items() if v["is_dark"]]


def get_light_themes() -> list:
    """Get names of light themes."""
    return [k for k, v in THEME_PALETTES.items() if not v["is_dark"]]
